package questao3;

public class Operacoes{
    public double somaValores(double num1, double num2){
        double soma=num1+num2;
        return soma;
    }

    public double subitraiValores(double num1, double num2){
        double subtracao=num1-num2;
        return subtracao;
    }

    public double divideValores(double num1, double num2){
        double divisao=num1/num2;
        return divisao;
    }

    public double multiplicaValores(double num1, double num2){
        double multiplicacao=num1*num2;
        return multiplicacao;
    }

}
